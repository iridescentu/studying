import java.util.Scanner;

public class Main {
    public static void main(String[] args) {
        // 백준 9655: 돌 게임
        // ------------------------------------------------------------------------
        // * 이 문제는 DP 없이 수학적 규칙으로 해결할 수 있음
        // * 상근이가 먼저 시작하므로, N이 홀수이면 상근이가 이기고, N이 짝수이면 창영이가 이김
        // * 이유:
        //   - 상근이가 돌을 1개 또는 3개 가져갈 수 있기 때문에, N이 홀수이면 마지막 돌을 상근이가 가져가게 됨
        //   - N이 짝수이면 창영이가 마지막 돌을 가져가게 됨
        // ------------------------------------------------------------------------

        Scanner sc = new Scanner(System.in);
        int N = sc.nextInt(); // 돌의 개수 N 입력 받음

        // N이 홀수이면 상근이가 이기고, 짝수이면 창영이가 이김
        if (N % 2 == 1) {
            System.out.println("SK");
        } else {
            System.out.println("CY");
        }
    }
}