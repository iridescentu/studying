import java.util.Scanner;

public class Main {
    public static void main(String[] args) {
        // 백준 1152: 단어의 개수
        // -------------------------------------------------------
        // 1. 문제 이해:
        // * 영어 대소문자와 공백으로 이루어진 문자열이 주어진다
        // * 문자열이 단어는 공백으로 구분된다
        // * 연속된 공백은 없으며, 문자열은 공백으로 시작하거나 끝날 수 있다.
        // * 문자열의 길이는 1,000,000을 넘지 않는다.
        // * 출력으로 문자열에 포함된 단어의 개수를 구한다.
        // 2. 접근 방법:
        // * 입력된 문자열의 공백을 기준으로 나누어 단어 배열을 만든다.
        // * 배열의 길이를 출력하여 단어의 개수를 구한다
        // 3. 구현 방법:
        // * Scanner 클래스를 사용하여 입력을 받는다.
        // * 입력된 문자열을 split("") 메서드를 사용해 공백을 기준으로 나눈다.
        // * 나누어진 단어 배열의 길이를 출력한다.
        // -------------------------------------------------------

        Scanner sc = new Scanner(System.in);

        // 입력된 문자열을 저장한다.
        String input = sc.nextLine().trim(); // 문자열 양쪽 끝의 공백 제거

        // 공백만 있는 문자열인지 확인한다.
        if (input.isEmpty()) {
            System.out.println(0);
        } else {
            // 입력된 문자열을 공백을 기준으로 나누어 단어 배열을 만든다.
            String[] words = input.split("\\s+"); // 연속된 공백을 하나의 공백으로 처리
            System.out.println(words.length);
        }
    }
}